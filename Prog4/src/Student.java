public class Student
{
	private String lname;
	private String fname;
	private String letterGrade;
	
	/**
	 * Student constructor
	 * @param fname students first name
	 * @param lname students last name
	 * @param letterGrade students letter grade
	 */
	public Student(String fname, String lname, String letterGrade) 
	{
		this.fname = fname;
		this.lname = lname;
		this.letterGrade = letterGrade;
	}

	/**
	 * @return lname the lname to return
	 */
	public String getLname()
		{
			return this.lname;
		}

	/**
	 * @param lname the lname to set
	 */
	public void setLname(String lname)
		{
			this.lname = lname;
		}

	/**
	 * @return fname the fname to return
	 */
	public String getFname()
		{
			return this.fname;
		}

	/**
	 * @param fname the fname to set
	 */
	public void setFname(String fname)
		{
			this.fname = fname;
		}

	/**
	 * @return the letterGrade
	 */
	public String getLetterGrade()
		{
			return letterGrade;
		}

	/**
	 * @param letterGrade the letterGrade to set
	 */
	public void setLetterGrade(String letterGrade)
		{
			this.letterGrade = letterGrade;
		}	
}
